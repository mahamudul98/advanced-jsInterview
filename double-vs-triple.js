// double & triple equal 

// for double equal (==) only check value 
const num1 = 4;
const num2 = '4';
if(num1==num2){
    console.log('condition is true');
}
else{
    console.log('condition is false');
}

// for triple equal(===) check value and check data type 
const num3 = 4;
const num4 = '4';
if(num3===num4){
    console.log('condition is true');
}
else{
    console.log('condition is false');
}